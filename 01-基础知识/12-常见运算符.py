# 5.逻辑运算符
'''
运算符    逻辑表达式
and       x and y
or        x or  y
not       not x
'''
a = 0
b = 1
c = 2

# 1. and：与：都真才真
print(a < b and c > b)
print(a > b and c < b)

# 2. or：或：一真则真，都假才假
print( a < b or c > b)
print( a > b or c > b)

# 3. not：非：取反
print(not False)
print(not c > b)

# 5.1 拓展   数字之间的逻辑运算
a = 0
b = 1
c = 2
# and运算符，只要有一个值为0，则结果为0，否则结果为最后一个非0数字
print(a and b)
print(b and a)
print(a and c)
print(c and a)
print(b and c)
print(c and b)

# or 运算符，只有所有值为0结果才为0，否则结果为第一个非0数字
print(a or b)
print(a or c)
print(b or c)

